#include "include.h"
//////////////////////////////////////////////////////////////////////////////////
//// 功能描述 : 1ms延时
//// 输入参数 : 
//// 返回参数 : 
//// 说明     :  
//////////////////////////////////////////////////////////////////////////////////
void delay(void)																								
{  
	INT8U i,j;
    for(i=30;i>0;i--)
	for(j=30;j>0;j--) ;
}
//////////////////////////////////////////////////////////////////////////////////
//// 功能描述 : ms级延时
//// 输入参数 : 
//// 返回参数 : 
//// 说明     :  
//////////////////////////////////////////////////////////////////////////////////
void delay_ms( INT16U cnt)																							
{  

	while(cnt > 0)
	{
       delay();
	   cnt--;
	}
}